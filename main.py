# Импортируем Path для построения путей к папкам
from pathlib import Path

# Импортируем модуль для работы с файловой системой
from src.fs_driver import FSDriver

# Импортируем модуль бизнес-логики
from src.business_logic import BusinessLogic

# Импортируем пользовательский интерфейс
from src.ui import UI

# Импортируем модуль логирования
from src.logger import setup_logger

# Импортируем модуль расширений (для будущих интеграций)
from src.extensions import Extensions


# Главная функция — точка входа в приложение
def main() -> None:
    """
    Главная функция приложения Notes.

    Выполняет:
    1. Определяет путь к папке данных
    2. Создаёт объекты всех модулей (ООП-архитектура)
    3. Запускает пользовательский интерфейс
    """
    # Получаем путь к текущей директории (где лежит main.py)
    project_root = Path(__file__).parent.resolve()

    # Определяем путь к папке для хранения данных заметок
    data_directory = project_root / "data"

    # Создаём логгер для главной функции
    logger = setup_logger("main")

    # Логируем начало работы приложения
    logger.info("=" * 50)
    logger.info("Запуск приложения Notes")
    logger.info("=" * 50)

    try:
        # Шаг 1: Создаём драйвер файловой системой
        # Он отвечает за чтение и запись файлов на диске
        fs_driver = FSDriver(data_directory)
        logger.info("Файловый драйвер создан")

        # Шаг 2: Создаём объект бизнес-логики
        # Он проверяет правила и управляет данными
        logic = BusinessLogic(fs_driver)
        logger.info("Бизнес-логика инициализирована")

        # Шаг 3: Создаём объект расширений (для будущих интеграций)
        extensions = Extensions(logic)
        logger.info("Модуль расширений подключён")

        # Шаг 4: Создаём и запускаем пользовательский интерфейс
        ui = UI(logic)
        logger.info("Запуск пользовательского интерфейса")
        ui.run()

    except Exception as e:
        # Если произошла критическая ошибка при запуске — логируем и показываем
        logger.critical(f"Критическая ошибка при запуске: {e}")
        print(f"\n  💥 Критическая ошибка: {e}")
        print("  Обратитесь к разработчику или проверьте лог-файл logs/notes.log")


# Проверяем, что этот файл запущен напрямую (а не импортирован как модуль)
if __name__ == "__main__":
    # Запускаем главную функцию
    main()
