# Импортируем модуль json для чтения и записи данных в файл
import json

# Импортируем модуль os для проверки существования файлов
import os

# Константа: имя файла, где храним все заметки
DATA_FILE = "notes.json"


# Функция: загрузить все заметки из файла
def load_notes():
    """
    Читает список заметок из JSON-файла.
    Если файла нет — возвращает пустой список.

    Возвращает:
        list: список строк (текстов заметок)
    """
    # Проверяем, существует ли файл с данными
    if os.path.exists(DATA_FILE):
        # Открываем файл для чтения с кодировкой UTF-8
        with open(DATA_FILE, "r", encoding="utf-8") as file:
            # Читаем JSON и превращаем обратно в список Python
            return json.load(file)

    # Если файла нет — возвращаем пустой список
    return []


# Функция: сохранить все заметки в файл
def save_notes(notes):
    """
    Записывает список заметок в JSON-файл.
    Перезаписывает весь файл текущим состоянием списка.

    Аргументы:
        notes (list): список строк для сохранения
    """
    # Открываем файл для записи (режим 'w' — перезапись)
    with open(DATA_FILE, "w", encoding="utf-8") as file:
        # Сохраняем список в JSON-формат с поддержкой русского текста
        json.dump(notes, file, ensure_ascii=False, indent=2)
