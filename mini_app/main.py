# Импортируем функции из модуля utils (добавление, просмотр, редактирование, удаление)
from utils import add_note, list_notes, edit_note, delete_note


# Главная функция — точка входа в приложение
def main():
    """
    Показывает меню и выполняет действия по выбору пользователя.
    Работает в бесконечном цикле до выбора пункта 'Выход'.
    """
    # Бесконечный цикл — программа работает, пока пользователь не решит выйти
    while True:
        # Печатаем красивое меню
        print("\n========== МЕНЮ ЗАМЕТОК ==========")
        print("  1. Добавить заметку")
        print("  2. Показать все заметки")
        print("  3. Редактировать заметку")
        print("  4. Удалить заметку")
        print("  5. Выход")
        print("==================================")

        # Запрашиваем у пользователя номер действия
        choice = input("  Ваш выбор (1-5): ").strip()

        # Обрабатываем выбор пользователя

        # Если выбран пункт 1 — добавляем заметку
        if choice == "1":
            add_note()

        # Если выбран пункт 2 — показываем все заметки
        elif choice == "2":
            list_notes()

        # Если выбран пункт 3 — редактируем заметку
        elif choice == "3":
            edit_note()

        # Если выбран пункт 4 — удаляем заметку
        elif choice == "4":
            delete_note()

        # Если выбран пункт 5 — выходим из программы
        elif choice == "5":
            print("  До свидания!")
            break

        # Если введено что-то непонятное — предупреждаем
        else:
            print("  Неверный выбор! Введите число от 1 до 5.")


# Проверяем, что этот файл запущен напрямую (а не импортирован как модуль)
if __name__ == "__main__":
    # Запускаем главную функцию
    main()
