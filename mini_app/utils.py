# Импортируем модули database и json для работы с данными
from database import load_notes, save_notes


# Функция: добавить новую заметку
def add_note():
    """
    Запрашивает у пользователя текст и добавляет его как новую заметку.
    Сохраняет заметку в файл через database.py.
    """
    # Спрашиваем у пользователя текст заметки
    text = input("  Введите текст заметки: ").strip()

    # Проверяем, что пользователь не оставил поле пустым
    if not text:
        # Если текст пустой — показываем ошибку и выходим из функции
        print("  Ошибка: заметка не может быть пустой!")
        return

    # Загружаем текущий список заметок из файла
    notes = load_notes()

    # Добавляем новый текст в конец списка
    notes.append(text)

    # Сохраняем обновлённый список обратно в файл
    save_notes(notes)

    # Сообщаем пользователю, что всё прошло успешно
    print(f"  Готово! Заметка добавлена. Всего заметок: {len(notes)}")


# Функция: показать все заметки
def list_notes():
    """
    Загружает все заметки из файла и выводит их на экран.
    Каждая заметка нумеруется начиная с 1.

    Возвращает:
        list: текущий список заметок (для использования в других функциях)
    """
    # Загружаем список заметок из файла
    notes = load_notes()

    # Если список пуст — сообщаем об этом и возвращаем пустой список
    if not notes:
        print("  Список заметок пуст!")
        return notes

    # Печатаем красивый заголовок с количеством заметок
    print(f"\n  --- Все заметки ({len(notes)} шт.) ---\n")

    # Перебираем все заметки и печатаем каждую с номером
    for i, note in enumerate(notes, 1):
        print(f"  {i}. {note}")

    # Печатаем пустую строку для красоты
    print()

    # Возвращаем список заметок (может пригодиться в main.py)
    return notes


# Функция: отредактировать заметку по номеру
def edit_note():
    """
    Показывает список, запрашивает номер и позволяет изменить текст заметки.
    Проверяет корректность ввода и сохраняет изменения в файл.
    """
    # Загружаем текущий список заметок
    notes = load_notes()

    # Если список пуст — редактировать нечего
    if not notes:
        print("  Список заметок пуст! Редактировать нечего.")
        return

    # Показываем пользователю все заметки с номерами
    list_notes()

    # Запрашиваем номер заметки для редактирования
    try:
        # Преобразуем ввод пользователя в число
        num = int(input("  Введите номер заметки для редактирования: "))

        # Проверяем, что номер в допустимом диапазоне
        if num < 1 or num > len(notes):
            print(f"  Ошибка: введите число от 1 до {len(notes)}!")
            return

    except ValueError:
        # Если пользователь ввёл не число — показываем ошибку
        print("  Ошибка: введите число!")
        return

    # Показываем текущий текст заметки
    old_text = notes[num - 1]
    print(f"  Текущий текст: {old_text}")

    # Запрашиваем новый текст заметки
    new_text = input("  Новый текст (Enter — оставить как есть): ").strip()

    # Если пользователь оставил поле пустым — ничего не меняем
    if not new_text:
        print("  Заметка не изменена.")
        return

    # Заменяем старый текст на новый в списке
    notes[num - 1] = new_text

    # Сохраняем обновлённый список в файл
    save_notes(notes)

    # Сообщаем об успешном редактировании
    print(f"  Заметка обновлена: '{old_text}' -> '{new_text}'")


# Функция: удалить заметку по номеру
def delete_note():
    """
    Показывает список заметок, запрашивает номер и удаляет выбранную.
    Проверяет корректность ввода (число, диапазон).
    """
    # Загружаем текущий список заметок
    notes = load_notes()

    # Если список пуст — удалять нечего, выходим
    if not notes:
        print("  Список заметок пуст! Удалять нечего.")
        return

    # Показываем пользователю все заметки с номерами
    list_notes()

    # Запрашиваем номер заметки для удаления
    try:
        # Преобразуем ввод пользователя в число
        num = int(input("  Введите номер заметки для удаления: "))

        # Проверяем, что номер в допустимом диапазоне (от 1 до длины списка)
        if num < 1 or num > len(notes):
            print(f"  Ошибка: введите число от 1 до {len(notes)}!")
            return

    except ValueError:
        # Если пользователь ввёл не число — показываем ошибку
        print("  Ошибка: введите число!")
        return

    # Запоминаем текст удаляемой заметки (чтобы показать пользователю)
    deleted_text = notes[num - 1]

    # Удаляем элемент из списка по индексу (номер минус 1, потому что с нуля)
    notes.pop(num - 1)

    # Сохраняем обновлённый список в файл
    save_notes(notes)

    # Сообщаем об успешном удалении
    print(f"  Заметка удалена: '{deleted_text}'")
