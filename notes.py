# Импортируем модуль json для сохранения заметок в файл
import json

# Импортируем модуль os для очистки экрана
import os

# Путь к файлу, где храним заметки
DATA_FILE = "notes_data.json"


# Функция: загрузить заметки из файла
def load_notes():
    """
    Читает заметки из JSON-файла.
    Если файла нет — возвращает пустой список.
    """
    # Проверяем, существует ли файл
    if os.path.exists(DATA_FILE):
        # Открываем файл и читаем данные
        with open(DATA_FILE, "r", encoding="utf-8") as f:
            return json.load(f)
    # Если файла нет — возвращаем пустой список
    return []


# Функция: сохранить заметки в файл
def save_notes(notes):
    """
    Записывает список заметок в JSON-файл.
    """
    # Открываем файл для записи и сохраняем список
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(notes, f, ensure_ascii=False, indent=2)


# Функция: добавить новую заметку
def add_note():
    """
    Спрашивает у пользователя текст заметки и добавляет в список.
    """
    # Запрашиваем текст заметки у пользователя
    text = input("  Введите текст заметки: ").strip()

    # Проверяем, что пользователь что-то ввёл
    if not text:
        print("  Ошибка: заметка не может быть пустой!")
        return

    # Загружаем текущий список заметок
    notes = load_notes()

    # Добавляем новую заметку в конец списка
    notes.append(text)

    # Сохраняем обновлённый список в файл
    save_notes(notes)

    # Сообщаем пользователю об успехе
    print(f"  Заметка добавлена! Всего заметок: {len(notes)}")


# Функция: вывести все заметки на экран
def list_notes():
    """
    Загружает все заметки и красиво выводит их на экран.
    """
    # Загружаем список заметок из файла
    notes = load_notes()

    # Если список пуст — сообщаем об этом
    if not notes:
        print("  Список заметок пуст!")
        return

    # Печатаем заголовок
    print(f"\n  --- Все заметки ({len(notes)} шт.) ---\n")

    # Перебираем все заметки и выводим с номером
    for i, note in enumerate(notes, 1):
        print(f"  {i}. {note}")

    # Печатаем разделитель
    print()


# Функция: отредактировать заметку по номеру
def edit_note():
    """
    Показывает список, запрашивает номер и позволяет изменить текст заметки.
    """
    # Загружаем текущий список заметок
    notes = load_notes()

    # Если список пуст — редактировать нечего
    if not notes:
        print("  Список заметок пуст! Редактировать нечего.")
        return

    # Показываем все заметки перед редактированием
    list_notes()

    # Запрашиваем номер заметки для редактирования
    try:
        num = int(input("  Введите номер заметки для редактирования: "))

        # Проверяем, что номер в допустимом диапазоне
        if num < 1 or num > len(notes):
            print(f"  Ошибка: введите число от 1 до {len(notes)}!")
            return

    except ValueError:
        # Если пользователь ввёл не число
        print("  Ошибка: введите число!")
        return

    # Показываем текущий текст заметки
    old_text = notes[num - 1]
    print(f"  Текущий текст: {old_text}")

    # Запрашиваем новый текст заметки
    new_text = input("  Новый текст (Enter — оставить как есть): ").strip()

    # Если пользователь оставил поле пустым — ничего не меняем
    if not new_text:
        print("  Заметка не изменена.")
        return

    # Заменяем старый текст на новый
    notes[num - 1] = new_text

    # Сохраняем обновлённый список в файл
    save_notes(notes)

    # Сообщаем об успешном редактировании
    print(f"  Заметка обновлена: '{old_text}' → '{new_text}'")


# Функция: удалить заметку по номеру
def delete_note():
    """
    Спрашивает номер заметки и удаляет её из списка.
    """
    # Загружаем текущий список заметок
    notes = load_notes()

    # Если список пуст — удалять нечего
    if not notes:
        print("  Список заметок пуст! Удалять нечего.")
        return

    # Показываем все заметки перед удалением
    list_notes()

    # Запрашиваем номер заметки для удаления
    try:
        num = int(input("  Введите номер заметки для удаления: "))

        # Проверяем, что номер в допустимом диапазоне
        if num < 1 or num > len(notes):
            print(f"  Ошибка: введите число от 1 до {len(notes)}!")
            return

    except ValueError:
        # Если пользователь ввёл не число
        print("  Ошибка: введите число!")
        return

    # Запоминаем текст удаляемой заметки (для сообщения)
    deleted = notes[num - 1]

    # Удаляем заметку из списка по индексу (номер - 1)
    notes.pop(num - 1)

    # Сохраняем обновлённый список в файл
    save_notes(notes)

    # Сообщаем об успешном удалении
    print(f"  Заметка удалена: '{deleted}'")


# Главная функция — показывает меню и обрабатывает выбор
def main():
    """
    Главная функция: выводит меню и выполняет действия по выбору пользователя.
    """
    # Бесконечный цикл работы программы
    while True:
        # Печатаем меню
        print("\n=== МЕНЮ ЗАМЕТОК ===")
        print("  1. Добавить заметку")
        print("  2. Показать все заметки")
        print("  3. Редактировать заметку")
        print("  4. Удалить заметку")
        print("  5. Выход")

        # Запрашиваем выбор пользователя
        choice = input("  Ваш выбор (1-5): ").strip()

        # Обрабатываем выбор
        if choice == "1":
            # Добавляем заметку
            add_note()
        elif choice == "2":
            # Показываем все заметки
            list_notes()
        elif choice == "3":
            # Редактируем заметку
            edit_note()
        elif choice == "4":
            # Удаляем заметку
            delete_note()
        elif choice == "5":
            # Выходим из программы
            print("  До свидания!")
            break
        else:
            # Если выбор не распознан — предупреждаем
            print("  Неверный выбор! Введите число от 1 до 5.")


# Проверяем, что файл запущен напрямую (а не импортирован)
if __name__ == "__main__":
    # Запускаем главную функцию
    main()
